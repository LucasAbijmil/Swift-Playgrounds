 # High Order Functions
 * [filter](filter) : Filtre les éléments d'une `Collection` en fonction d'une ou plusieurs condition(s)
 * [map](map) : Applique une transformation à chaque élément d'une `Collection`
 * [reduce](reduce) : Combine tous les éléments d'une `Collection` dans une valeur
 * [compactMap](compactMap) : Unwrap les optionnels et supprime les éléments `nil` d'une `Collection`
 * [flatMap](flatMap) : Réduit d'une dimension la dimension d'une `Collection` en concaténant ses éléments
 * [contains](contains) : Indique si la `Collection` contient au moins une fois l'élément passé en paramètre ou contient au moins un élément satisfaisant la ou les conditions dans la closure



 ## Avantage commun à toutes les High Order Functions
 * Le résultat d'une HOF peut être stocké dans une constante, rendant le résultat immutable par la suite
